package com.blockchyp.client.dto;

public class RawPublicKey {
    
    private String curve;
    private String x;
    private String y;
    public String getCurve() {
        return curve;
    }
    public void setCurve(String curve) {
        this.curve = curve;
    }
    public String getX() {
        return x;
    }
    public void setX(String x) {
        this.x = x;
    }
    public String getY() {
        return y;
    }
    public void setY(String y) {
        this.y = y;
    }
    
    
    

}
